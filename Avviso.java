public class Avviso {

    int numero;
    String data;
    int insegnante;
    int classe;
    String testo;

    public Avviso() {}

    public Avviso(int numero, String data, int insegnante, int classe, String testo) {

        this.numero = numero;
        this.data = data;
        this.insegnante = insegnante;
        this.classe = classe;
        this.testo = testo;
    }
}
 
