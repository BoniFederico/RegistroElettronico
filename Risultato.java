public class Risultato {

    String data;
    int ora;
    int insegnante;
    int studente;
    int voto;



    public Risultato() {}

    public Risultato(String data, int ora, int insegnante, int studente, int voto) {
        this.data = data;
        this.ora = ora;
        this.insegnante = insegnante;
        this.voto = voto;
        this.studente = studente;

    }

}
