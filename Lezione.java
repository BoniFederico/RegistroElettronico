public class Lezione {
    String data;
    int ora;
    int locazione;
    String materia;
    int insegnante;
    String argomento;

    public Lezione() {}

    public Lezione(String data, int ora, int locazione, String materia, int insegnante, String argomento) {

        this.data = data;
        this.ora = ora;
        this.locazione = locazione;
        this.materia = materia;
        this.insegnante = insegnante;
        this.argomento = argomento;
    }
}
